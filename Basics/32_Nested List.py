fruits=["Strawberry","Nectarines","Apples","Grapes","Peaches","Cherries","Pears"]
vegetables=["Spinach","Kale","Tomatoes","Celery","Potatoes"]

dirty_dozen=[fruits,vegetables]   #Combines two lists and makes a new list
print(dirty_dozen)    #dirty dozen is a nested list
print(dirty_dozen[1])   #prints the second list
print(dirty_dozen[0])   #prints the first list
print(dirty_dozen[0][3])    #prints the element with index 3 in list 1

